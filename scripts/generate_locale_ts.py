def parse_en_keys() -> dict[str, str]:
    src = I18N.read_text(encoding="utf-8")
    block = re.search(r"const en = \{([\s\S]*?)\} as const", src)
    if not block:
        raise SystemExit("Could not parse en block")
    en: dict[str, str] = {}
    for m in re.finditer(r'^\s+(\w+):\s*"((?:[^"\\]|\\.)*)"', block.group(1), re.M):
        en[m.group(1)] = m.group(2)
    return en


def ts_escape(s: str) -> str:
    return s.replace("\\", "\\\\").replace('"', '\\"')


def write_locale(path: Path, export_name: str, locale: dict[str, str]) -> None:
    lines = [f'export const {export_name}: Record<string, string> = {{']
    for key in sorted(locale.keys()):
        lines.append(f'    {key}: "{ts_escape(locale[key])}",')
    lines.append("};")
    lines.append("")
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text("\n".join(lines), encoding="utf-8")


